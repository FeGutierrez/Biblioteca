public class Editorial {
    
    private int id;
    private String nombre;
    private int nit;

    public Editorial(String nombre, int nit) {
        this.nombre = nombre;
        this.nit = nit;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
    

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getNit() {
        return nit;
    }

    public void setNit(int nit) {
        this.nit = nit;
    }
    
    
    
    
}
